function createFireParticles(tex, quad)
    emitter = love.graphics.newParticleSystem(tex,10)
    emitter:setDirection(1.6000000238419)
    emitter:setAreaSpread("normal",7,1)
    emitter:setEmissionRate(10)
    emitter:setEmitterLifetime(-1)
    emitter:setLinearAcceleration(0,0,0,0)
    emitter:setParticleLifetime(1,1)
    emitter:setRadialAcceleration(0,0)
    emitter:setRotation(0,0)
    emitter:setTangentialAcceleration(0,0)
    emitter:setSpeed(30,50)
    emitter:setSpin(0,0)
    emitter:setSpinVariation(2.7755575615629e-017)
    emitter:setLinearDamping(0,0)
    emitter:setSpread(0.60000002384186)
    emitter:setRelativeRotation(false)
    emitter:setOffset(5,5)
    emitter:setSizes(1)
    emitter:setSizeVariation(0)
    emitter:setColors(255,79,0,190, 255,5,25,176, 76,76,76,19 )
    emitter:setQuads(quad)
    return emitter;
end